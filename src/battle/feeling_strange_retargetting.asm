
FEELING_STRANGE_RETARGETTING: ;$C24009
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE_CLOBBER 18
	MOVE_INT_CONSTANT 0, BATTLER_TARGET_FLAGS
	LDX CURRENT_ATTACKER
	LDA a:battler::action_targetting,X
	AND #$00FF
	AND #$0007
	CMP #$0001
	BEQ @UNKNOWN0
	CMP #$0002
	BEQ @UNKNOWN1
	CMP #$0004
	BEQ @UNKNOWN2
	BRA @UNKNOWN5
@UNKNOWN0:
	JSL TARGET_ALL
	MOVE_INT BATTLER_TARGET_FLAGS, $06
	MOVE_INT $06, $0E
	JSL RANDOM_TARGETTING
	MOVE_INT $06, BATTLER_TARGET_FLAGS
	BRA @UNKNOWN5
@UNKNOWN1:
	JSL RAND
	LDY #$0003
	JSL MODULUS16
	JSL TARGET_ROW
	BRA @UNKNOWN5
@UNKNOWN2:
	JSL RAND
	AND #$0001
	BEQ @UNKNOWN3
	JSL TARGET_ALLIES
	BRA @UNKNOWN4
@UNKNOWN3:
	JSL TARGET_ALL_ENEMIES
@UNKNOWN4:
	LDX CURRENT_ATTACKER
	LDA a:battler::current_action,X
	JSL GET_SHIELD_TARGETTING
	CMP #$0000
	BNE @UNKNOWN5
	LDX CURRENT_ATTACKER
	LDA a:battler::ally_or_enemy,X
	AND #$00FF
	BNE @UNKNOWN5
	JSL REMOVE_NPC_TARGETTING
@UNKNOWN5:
	PLD
	RTS
